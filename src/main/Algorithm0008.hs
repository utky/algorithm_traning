{-
 - 問題 †
 - あなたのチームは、n個の仕事を抱えています。それぞれの仕事(i)は、時間Siに始まり、時間Tiに終わります。あなたは、できるだけ多くの仕事に参加したいと考えています。
 - 仕事に参加するならば、その仕事の初めから終わりまで参加しなければなりません。
 - また、参加する仕事の時間帯が重なってはなりません。
 - （開始の瞬間・終了の瞬間だけが重なるのも許されません。）
 - 　
 - 出来るだけ多くの仕事に参加できるパターンを抽出してくれるプログラムを作成しなさい。
 - 各仕事の開始/終了時刻は、整数とし任意に入力出来るものとします。入力した仕事は、入力順に1から始まる整数でナンバリングします。
 - 回答は、参加可能な仕事の最大の数と仕事の番号を出力するようにしてください。
 - 複数のパターンが考えられる場合は、そのうちの一つで問題ありません。
 - 
 - ↑
 - 制約 †
 - １≦n≦100000
 - １≦Si＜Ti≦10^9
 - 
 - ↑
 - 例 †
 - N=5
 - S1=1 T1=3
 - S2=2 T2=5
 - S3=4 T3=7
 - S4=6 T4=9
 - S5=8 T5=10
 - 
 - Ans: 3
 - Items: 1 3 5
 -}
